export const getUnitLabel = (unit: string, quantity: number): string => {
  const unitLabels: { [key: string]: { singular: string; plural: string } } = {
    piece: { singular: 'piece', plural: 'pieces' },
    cup: { singular: 'cup', plural: 'cups' },
    bowl: { singular: 'bowl', plural: 'bowls' },
    plate: { singular: 'plate', plural: 'plates' },
    glass: { singular: 'glass', plural: 'glasses' },
    tablespoon: { singular: 'tbsp', plural: 'tbsp' },
    teaspoon: { singular: 'tsp', plural: 'tsp' },
    grams: { singular: 'g', plural: 'g' },
    ml: { singular: 'ml', plural: 'ml' },
    serving: { singular: 'serving', plural: 'servings' },
    slice: { singular: 'slice', plural: 'slices' },
    packet: { singular: 'packet', plural: 'packets' },
    scoop: { singular: 'scoop', plural: 'scoops' },
  };

  const labels = unitLabels[unit] || { singular: unit, plural: unit };
  return quantity === 1 ? labels.singular : labels.plural;
};
